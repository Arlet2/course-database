package su.arlet.entities

import org.jetbrains.exposed.sql.*


data class BatchDelivery(
    val deliveryID: Int,
    val batchID: Int,
    val amount: Int,
    val mass: Double?
)

object BatchDeliveries : Table("batch_delivery") {
    val deliveryID = integer("delivery_id")
        .references(Deliveries.id, onDelete = ReferenceOption.CASCADE)
    val batchID = integer("batch_id")
        .references(Batches.id, onDelete = ReferenceOption.CASCADE)
    val amount = integer("amount")
        .check("positive_amount") { it.greaterEq(0) }
    val mass = double("mass").nullable()
        .check("positive_mass") { it.isNull().or(it.greaterEq(0.0)) }

    override val primaryKey = PrimaryKey(deliveryID, batchID)
}

